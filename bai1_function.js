function tinhTong(a,b)
{
    const ketQua = a + b
    return ketQua
}

function tinhHieu(a,b)
{
    const ketQua = a - b
    return ketQua
}

function tinhTich(a, b)
{
    const ketQua = a * b
    return ketQua
}

function tinhThuong(a, b)
{
    const ketQua = a/b
    return ketQua
}

